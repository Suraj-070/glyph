// Master valid-guess list (union, uppercased, strict 5-letter filter)
const VALID_SET = new Set(
  [...ANSWER_WORDS, ...EXTRA_VALID]
    .map((w) => w.toUpperCase().replace(/[^A-Z]/g, ""))
    .filter((w) => w.length === 5)
);

// Answer words filtered strictly to 5 letters
const ANSWER_CLEAN = ANSWER_WORDS
  .map((w) => w.toUpperCase().replace(/[^A-Z]/g, ""))
  .filter((w) => w.length === 5);

export function isValidWord(word: string): boolean {
  return VALID_SET.has(word.toUpperCase());
}

export function randomAnswer(exclude?: string): string {
  let w = ANSWER_CLEAN[Math.floor(Math.random() * ANSWER_CLEAN.length)];
  if (exclude) {
    let guard = 0;
    while (w === exclude && guard < 10) {
      w = ANSWER_CLEAN[Math.floor(Math.random() * ANSWER_CLEAN.length)];
      guard++;
    }
  }
  return w;
}

// Deterministic daily word from a date (so everyone gets the same word, server-side authoritative).
export function dailyWordForDate(date: Date): string {
  const y = date.getFullYear();
  const m = date.getMonth() + 1;
  const d = date.getDate();
  const seed = y * 10000 + m * 100 + d;
  const idx = seed % ANSWER_CLEAN.length;
  return ANSWER_CLEAN[idx];
}

// Daily challenge type rotation (normal / challenge / hardcore)
export function dailyTypeForDate(date: Date): "normal" | "challenge" | "hardcore" {
  const day = date.getDay(); // 0 Sun .. 6 Sat
  // Tue & Thu -> challenge, Fri -> hardcore, else normal
  if (day === 5) return "hardcore";
  if (day === 2 || day === 4) return "challenge";
  return "normal";
}
